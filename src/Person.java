public class Person {
  String name;
  int age;

  public Person(String name, int age){
    this.name = name;
    this.age = age;
  }

  //This method sets the persons name
  public void setName(String pName){
    this.name = pName;
  }
  
  //This sets the persons age
  public void setAge(int pAge){
    this.age = pAge;
  }


}
